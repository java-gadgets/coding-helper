package org.pplm.gadgets.codinghelper.entity.metadata;
/**
 * 
 * @author OracleGao
 *
 */
public class EntityAttr {
	private String name;
	private String type;
	
	public EntityAttr() {
		super();
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
}
